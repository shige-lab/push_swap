#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new;

	if (!lst || !(*f))
		return (NULL);
	new = ft_lstnew((*f)(lst->content));
	new_list = new;
	lst = lst->next;
	while (lst)
	{
		new->next = ft_lstnew((*f)(lst->content));
		if (!new->next)
		{
			ft_lstclear(&new, (*del));
			return (NULL);
		}
		new = new->next;
		lst = lst->next;
	}
	return (new_list);
}
